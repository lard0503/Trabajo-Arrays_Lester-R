    private void jBtnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEditarActionPerformed
        // TODO add your handling code here:
        this.dPO.getListProducto().get(pos).setID(Integer.parseInt(jTfId.getText()));
        this.dPO.getListProducto().get(pos).setNombre(jTfNombre.getText());
        this.dPO.getListProducto().get(pos).setPrecio(Double.parseDouble(jTfPrecio.getText()));
        this.dPO.getListProducto().get(pos).setExistencia(Double.parseDouble(jTfExistencia.getText()));
        JOptionPane.showMessageDialog(this, "Cambios realizados",
                "Editar", JOptionPane.INFORMATION_MESSAGE);
        llenarTabla();
        limpiar();
    }//GEN-LAST:event_jBtnEditarActionPerformed

    private void jBtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEliminarActionPerformed
        // TODO add your handling code here:
        int op;
        op = JOptionPane.showConfirmDialog(this,
                "Desea Eliminar?", "Eliminar", JOptionPane.YES_NO_OPTION);
        if (op == 0) {
                dPO.getListProducto().remove(pos);
            JOptionPane.showMessageDialog(this,
                    "OK");
            llenarTabla();
            limpiar();
        } else {
            JOptionPane.showMessageDialog(this,
                    "Operacion Cancelada");
        }
    }//GEN-LAST:event_jBtnEliminarActionPerformed

    private void llenarTabla() {
        DefaultTableModel tbl = new DefaultTableModel();
        tbl = dPO.getListProduc();
        jTblRegistros.setModel(tbl);
    }

    private void limpiar() {
        jTfId.setText("");
        jTfNombre.setText("");
        jTfPrecio.setText("");
        jTfExistencia.setText("");
        jBtnAgregar.setEnabled(true);
        jBtnEditar.setEnabled(false);
        jBtnEliminar.setEnabled(false);
        jTfId.requestFocus();
    }

