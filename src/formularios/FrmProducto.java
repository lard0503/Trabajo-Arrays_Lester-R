    private void jBtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAgregarActionPerformed
        // TODO add your handling code here:
        if (jTfId.getText().length() == 0) {
            JOptionPane.showMessageDialog(this,
                    "Necesita agregar el ID del producto",
                    "Error",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (jTfNombre.getText().length() == 0) {
            JOptionPane.showMessageDialog(this,
                    "Necesita agregar el Nombre del producto",
                    "Error",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }
        int id = Integer.parseInt(jTfId.getText());
        String nom = jTfNombre.getText();
        double precio = Double.parseDouble(jTfPrecio.getText());
        double exist = Double.parseDouble(jTfExistencia.getText());

        int b = dPO.agregarProducto(id, nom, precio, exist);
        if (b == 1) {
            JOptionPane.showMessageDialog(this,
                    "Registro Agregado...",
                    "Guardar",
                    JOptionPane.INFORMATION_MESSAGE);
            limpiar();
        } else {
            JOptionPane.showMessageDialog(this,
                    "Error al guardar...",
                    "Guardar",
                    JOptionPane.ERROR_MESSAGE);
        }
        llenarTabla();
    }//GEN-LAST:event_jBtnAgregarActionPerformed

