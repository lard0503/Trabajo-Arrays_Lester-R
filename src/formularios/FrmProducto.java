    private void jBtnAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAnteriorActionPerformed
        // TODO add your handling code here:
        if (!dPO.getListProducto().isEmpty()) {
            try {
                pos--;
                if (pos < 0) {
                    pos = dPO.getListProducto().size() - 1;
                }
                int id = dPO.getListProducto().get(pos).getID();
                String nom = dPO.getListProducto().get(pos).getNombre();
                double precio = dPO.getListProducto().get(pos).getPrecio();
                double exist = dPO.getListProducto().get(pos).getExistencia();

                jTfId.setText("" + id);
                jTfNombre.setText(nom);
                jTfPrecio.setText("" + precio);
                jTfExistencia.setText("" + exist);

                jBtnAgregar.setEnabled(false);
                jBtnEditar.setEnabled(true);
                jBtnEliminar.setEnabled(true);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage(),
                        "Error", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(this, "No hay registros...",
                    "Registros", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jBtnAnteriorActionPerformed

